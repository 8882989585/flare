  private def minSwaps(grid: Array[Array[Int]]): Int = {
    import scala.collection.mutable
    val map = mutable.TreeMap[Int, mutable.HashSet[Int]]()
    for (j <- grid(0).length - 1 to 1 by -1) {
      val set = mutable.HashSet[Int]()
      val previousSet = map.getOrElse(j + 1, mutable.HashSet[Int]((for (i <- grid.indices) yield i).toList: _*))
      for (i <- grid.indices) {
        if (grid(i)(j) == 0 && previousSet.contains(i))
          set.add(i)
      }
      map(j) = set
    }
    var hashSet = mutable.HashSet[Int]()
    for (i <- map.iterator) {
      hashSet = hashSet ++ i._2
      if (hashSet.size < i._1) {
        return -1
      }
    }
    val usedSet = Array.fill[Int](grid.length + 1)(Int.MaxValue)
    for (i <- map.iterator) {
      for (j <- i._2) {
        usedSet(j + 1) = Math.min(i._1, usedSet(j + 1))
      }
    }
    var count = 0
    for (i <- 1 to grid.length) {
      if (usedSet(i) > i) {
        breakable {
          for (j <- i + 1 to grid.length) {
            if (usedSet(j) <= i) {
              val t = usedSet(j)
              for (k <- j until i by -1) {
                usedSet(k) = usedSet(k - 1)
                count = count + 1
              }
              usedSet(i) = t
              break
            }
          }
        }
      }
    }
    count
  }

